package homework.day2.basetask.returnTask;

public class TrainMethodsReturn {
    public static int returnNewInt(int a) {
        return a * 3;
    }

    public static long returnNewLong(long a) {
        return a - 4;
    }

    public static char returnNewChar(char a) {
        return a; //как вывести строку из двух таких же символов
    }

    public static float returnNewFloat(float a) {
        return a / 2;
    }

    public static double returnNewDouble(double a) {
        return a + 8;
    }

    public static short returnNewShort(short a) {
        return (short) (a - 1);
    }

    public static byte returnNewByte(byte a) {
        return (byte) (a * 2);
    }

    public static boolean returnNewBoolean(boolean a) {
        return !a;
    }

}
