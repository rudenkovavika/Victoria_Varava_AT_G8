package homework.day2.basetask.primitiveTask;

public class TrainMethodsPrimitive {
    public static void printInt(int a) {
        System.out.println("я получил на вход число " + a);
    }

    public static void printLong(long a) {
        System.out.println("я получил на вход длинное число " + a);
    }

    public static void printChar(char a) {
        System.out.println("я получил на вход символ " + a);
    }

    public static void printFloat(float a) {
        System.out.println("я получил на вход дробное число " + a);
    }

    public static void printDouble(double a) {
        System.out.println("я получил на вход длинное дробное число " + a);
    }

    public static void printShort(short a) {
        System.out.println("я получил на вход короткое число " + a);
    }

    public static void printByte(byte a) {
        System.out.println("я получил на вход очень короткое число " + a);
    }

    public static void printBoolean(boolean a) {
        System.out.println("я получил на вход булево " + a);
    }
}