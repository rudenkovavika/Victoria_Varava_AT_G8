package pretask;

public class MathOperatorsExample {
    public static void main(String[] args) {
        int a = 9;
        int b = 3;

        int sum = a + b;
        int sub = a - b;
        int mul = a * b;
        int div = a / b;

        System.out.println("Сложение: " + sum);
        System.out.println("Вычитание: " + sub);
        System.out.println("Умножение: " + mul);
        System.out.println("Деление: " + div);
    }
}
